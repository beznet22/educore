// =============================================================================
// ContentShareListAudience (owned by ContentShareList)
// =============================================================================

/// A materialized audience descriptor for a
/// [`ContentShareList`](crate::aggregate::ContentShareList).
/// Captured at dispatch time. The send_type + the matching
/// recipient id list. Per `docs/specs/cms/entities.md`.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct ContentShareListAudience {
    /// The typed id.
    pub id: ContentShareListAudienceId,
    /// The owning share list id (FK to the parent aggregate).
    pub share_list_id: ContentShareListId,
    /// The owning school.
    pub school_id: SchoolId,
    /// The role ids (used when `send_type = G`).
    pub role_ids: Vec<Uuid>,
    /// The user ids (used when `send_type = I`).
    pub user_ids: Vec<Uuid>,
    /// The class id (used when `send_type = C`).
    pub class_id: Option<Uuid>,
    /// The section ids (used when `send_type = C`).
    pub section_ids: Vec<Uuid>,
    /// The creation timestamp.
    pub created_at: DateTime<Utc>,
    /// The user who materialised the audience.
    pub created_by: UserId,
}

// =============================================================================
// ContentShareListContent (owned by ContentShareList)
// =============================================================================

/// A join between a [`ContentShareList`](crate::aggregate::ContentShareList)
/// and a [`Content`](crate::aggregate::Content). The list has
/// many contents; each content appears in many lists.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct ContentShareListContent {
    /// The typed id.
    pub id: ContentShareListContentId,
    /// The owning share list id (FK to the parent aggregate).
    pub share_list_id: ContentShareListId,
    /// The content id being shared.
    pub content_id: ContentId,
    /// The owning school.
    pub school_id: SchoolId,
    /// The position of the content within the list (0-indexed).
    pub position: u32,
    /// The creation timestamp.
    pub created_at: DateTime<Utc>,
    /// The user who added the content to the list.
    pub created_by: UserId,
}

