//! CMS-domain child entities.
//!
//! Per `docs/specs/cms/entities.md`. Entities have identity and
//! lifecycle but are not aggregate roots. They are loaded and
//! persisted only through their aggregate root.
//!
//! The CMS domain ships 6 child entities:
//! - `NewsImage` (owned by `News`)
//! - `ContentShareListAudience` (owned by `ContentShareList`)
//! - `ContentShareListContent` (owned by `ContentShareList`)
//! - `PageRevision` (owned by `Page`)
//! - `NewsRevision` (owned by `News`)
//! - `ContentRevision` (owned by `Content`)
//!
//! Per the spec, several value-object children (`PageSettings`,
//! `ContentAvailability`, `TeacherUploadContentScope`,
//! `UploadContentScope`, `HomeSliderOrder`, `TestimonialImage`)
//! are embedded in the parent aggregate and are not separate
//! entities; the typed wrappers live in the aggregate file.

#![allow(dead_code, clippy::all)]
#![allow(missing_docs)]

use chrono::{DateTime, Utc};
use educore_core::ids::{SchoolId, UserId};
use serde::{Deserialize, Serialize};

use crate::value_objects::{
    ContentId, ContentRevisionId, ContentShareListAudienceId, ContentShareListContentId,
    ContentShareListId, FileReference, NewsId, NewsImageId, NewsRevisionId, PageId, PageRevisionId,
};

// =============================================================================
// NewsImage (owned by News)
// =============================================================================

/// A news-image child entity. The `image` and `image_thumb`
/// references for a [`News`](crate::aggregate::News) entry. Both
/// are [`FileReference`]s; the thumb is generated by an image
/// processing port (deferred to Phase 15). The child entity has
/// its own typed id (`NewsImageId`) but is loaded and persisted
/// only through its aggregate root.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct NewsImage {
    /// The typed id.
    pub id: NewsImageId,
    /// The owning news id (FK to the parent aggregate).
    pub news_id: NewsId,
    /// The owning school.
    pub school_id: SchoolId,
    /// The image file.
    pub image: FileReference,
    /// The optional thumbnail file.
    pub image_thumb: Option<FileReference>,
    /// The creation timestamp.
    pub created_at: DateTime<Utc>,
    /// The last-update timestamp.
    pub updated_at: DateTime<Utc>,
    /// The creating user.
    pub created_by: UserId,
    /// The last-updating user.
    pub updated_by: UserId,
}

// =============================================================================
// PageRevision (owned by Page)
// =============================================================================

/// A historical snapshot of a [`Page`](crate::aggregate::Page)
/// body. Revisions are append-only and allow reverting the page
/// to a prior state. The child entity has its own typed id
/// (`PageRevisionId`) but is loaded and persisted only through
/// its aggregate root.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct PageRevision {
    /// The typed id.
    pub id: PageRevisionId,
    /// The owning page id (FK to the parent aggregate).
    pub page_id: PageId,
    /// The owning school.
    pub school_id: SchoolId,
    /// The revision body (the page's body at the time of the
    /// snapshot).
    pub body: String,
    /// The revision number (monotonically increasing per page).
    pub revision_number: u32,
    /// The creation timestamp.
    pub created_at: DateTime<Utc>,
    /// The user who created the revision.
    pub created_by: UserId,
}

